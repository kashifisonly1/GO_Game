public class BeadLOC {
    private int x;
    private int y;
    // Constructor
    public BeadLOC(int i, int j) {
        x = i;
        y = j;
    }
    
    // Getters
    public int getX() {
        return x;
    }
    
    public int getY() {
        return y;
    }
    
}
